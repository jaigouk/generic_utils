package helpers

import (
	"sort"

	"golang.org/x/exp/constraints"
)

// Contains returns true if element is in array
func Contains[T comparable](elems []T, v T) bool {
	for _, s := range elems {
		if v == s {
			return true
		}
	}
	return false
}

// Keys returns keys of map
func Keys[K comparable, V any](m map[K]V) []K {
	keys := make([]K, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	return keys
}

// Filter returns elements of array that match predicate
func Filter[T any](slice []T, f func(T) bool) []T {
	var n []T
	for _, e := range slice {
		if f(e) {
			n = append(n, e)
		}
	}
	return n
}

// SortSlice returns sorted slice
func SortSlice[T constraints.Ordered](s []T) {
	sort.Slice(s, func(i, j int) bool {
		return s[i] < s[j]
	})
}

// MapSlice returns map of elements
func MapSlice[T any, M any](a []T, f func(T) M) []M {
	n := make([]M, len(a))
	for i, e := range a {
		n[i] = f(e)
	}
	return n
}

// Reduce returns reduced value of elements
func Reduce[T, M any](s []T, f func(M, T) M, in M) M {
	acc := in
	for _, v := range s {
		acc = f(acc, v)
	}
	return acc
}

// Uniq removes duplications in slice
func Uniq[T comparable](s []T) []T {
	inResult := make(map[T]bool)
	var result []T
	for _, str := range s {
		if _, ok := inResult[str]; !ok {
			inResult[str] = true
			result = append(result, str)
		}
	}
	return result
}
